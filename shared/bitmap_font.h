inline void draw_char(uint8_t* framebuffer, int fw, int fh, int x, int y, char c, uint8_t r, uint8_t g, uint8_t b) {
    int idx = static_cast<int>(c) - 32;
    if (idx < 0 || idx >= 95) return;
    
    const uint8_t* glyph = &BITMAP_FONT_DATA[idx * 5];
    
    for (int row = 0; row < 5; ++row) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 3; ++col) {
            if (bits & (1 << (2 - col))) {
                int px = x + col;
                int py = y + row;
                if (px >= 0 && px < fw && py >= 0 && py < fh) {
                    int pi = (py * fw + px) * 4;
                    framebuffer[pi + 0] = r;
                    framebuffer[pi + 1] = g;
                    framebuffer[pi + 2] = b;
                    framebuffer[pi + 3] = 255;
                }
            }
        }
    }
}

